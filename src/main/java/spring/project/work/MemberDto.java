package spring.project.work;

public class MemberDto {
	
	private String name;
	private String birth;
	private String id;
	private String pwd;
	private String email;
	private String gender;
	private String addr;
	private String phone;
	private int point;
	private String team_name;
	private int team_int;
	private int team_grade;
	private int grade;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public String getTeam_name() {
		return team_name;
	}
	public void setTeam_name(String team_name) {
		this.team_name = team_name;
	}
	
	public int getTeam_int() {
		return team_int;
	}
	public void setTeam_int(int team_int) {
		this.team_int = team_int;
	}
	public int getTeam_grade() {
		return team_grade;
	}
	public void setTeam_grade(int team_grade) {
		this.team_grade = team_grade;
	} 
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	
}
